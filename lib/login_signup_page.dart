class LoginSignupPage extends StatefulWidget {
  LoginSignupPage({this.auth, this.loginCallback});

  final BaseAuth auth;
  final VoidCallback loginCallback;

  @override
  State<StatefulWidget> createState() => new _LoginSignupPageState();
}

class _LoginSignupPageState extends State<LoginSignupPage> {
  final _formKey = new GlobalKey<FormState>();

  String _status = '';
  String _email;
  String _password;
  String _errorMessage;

  bool _isLoginForm;
  bool _isLoading;

  static FirebaseDatabase database = new FirebaseDatabase();
  DatabaseReference _userRef = database.reference();

  @override
  void initState() {
    _errorMessage = "";
    _isLoading = false;
    _isLoginForm = true;
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return new Scaffold(
        appBar: new AppBar(
          title: new Text('Flutter login demo'),
        ),
        body: Stack(
          children: <Widget>[
            Text(this._status),
            _showForm(),
            _showCircularProgress(),
          ],
        ));
  }

  // Perform login or signup
  void validateAndSubmit() async {
    setState(() {
      _errorMessage = "";
      _isLoading = true;
    });
    if (validateAndSave()) {
      String userId = "";
      try {
        if (_isLoginForm) {
          userId = await widget.auth.signIn(_email, _password);
          print('Signed in: $userId');

          //TODO chez Wunderlist on se base sur l'adresse email pour faire le match,
          // du coup il faudrait que je save {email, name, rights} pour chaque personne a qui je partage
          // pour ca il faut un formulaire ou on peut renseigner l'adresse email de la personne avec qui on veut partager
          // et une liste des personnes à qui on a partagé

          _userRef.child("users/$userId").push().set(<String, String>{
            "email": _email,
            "password": _password,
          }).then((_) {
            print('Transaction  committed.');
          });
        } else {
          userId = await widget.auth.signUp(_email, _password);
          //widget.auth.sendEmailVerification();
          //_showVerifyEmailSentDialog();
          print('Signed up user: $userId');

          _userRef.child("users/$userId").push().set(<String, String>{
            "email": _email,
            "password": _password,
          }).then((_) {
            print('Transaction  committed.');
          });
        }
        setState(() {
          _isLoading = false;
          _status = "Connected as $userId";
        });

        // if (userId.length > 0 && userId != null && _isLoginForm) {
        //   widget.loginCallback();
        // }
      } catch (e) {
        print('Error: $e');
        setState(() {
          _isLoading = false;
          _errorMessage = e.message;
          _formKey.currentState.reset();
        });
      }
    }
  }

    // Check if form is valid before perform login or signup
  bool validateAndSave() {
    final form = _formKey.currentState;
    if (form.validate()) {
      form.save();
      return true;
    }
    return false;
  }

  void resetForm() {
    _formKey.currentState.reset();
    _errorMessage = "";
  }

  void toggleFormMode() {
    resetForm();
    setState(() {
      _isLoginForm = !_isLoginForm;
    });
  }

  Widget _showCircularProgress() {
    if (_isLoading) {
      return Center(child: CircularProgressIndicator());
    }
    return Container(
      height: 0.0,
      width: 0.0,
    );
  }

